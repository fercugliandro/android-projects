package com.fercugliandro.blacklist.datatype;

public class Blacklist {
	
	private int id;
	private String numero;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
}
